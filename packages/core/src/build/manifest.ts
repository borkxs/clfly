import { mkdirSync, writeFileSync, readFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { pathToFileURL, fileURLToPath } from "node:url";
import type { FlagInfo, Manifest, ManifestRoute, Meta } from "../types.js";
import { MANIFEST_FORMAT_VERSION } from "../types.js";
import {
  listCommandFiles,
  loadAndValidateCommand,
  scanCommandsDir,
} from "../router/scan.js";
import { projectFlags } from "../schema/to-json-schema.js";
import { coreMajorFromVersion, fileUrlToPath } from "../version.js";

export interface BuildManifestOptions {
  /** Commands directory (path or file URL). */
  commandsDir: string | URL;
  /** Output path for the generated manifest module (`.js` or `.ts`). */
  outFile: string | URL;
  /**
   * Version of @clfly/core writing the manifest. Defaults to this package's version.
   * Used for `coreMajor` and mismatch error text.
   */
  coreVersion?: string;
  /** Override import path style; default is relative from outFile to each command file. */
  toImportPath?: (commandFile: string, outFile: string) => string;
}

export interface BuildManifestResult {
  outFile: string;
  manifest: Manifest;
  routeCount: number;
}

/** Scan, validate reserved flags, and emit a lazy-load manifest module. */
export async function buildManifest(
  options: BuildManifestOptions,
): Promise<BuildManifestResult> {
  const commandsDir = fileUrlToPath(options.commandsDir);
  const outFile = fileUrlToPath(options.outFile);
  const coreVersion = options.coreVersion ?? readCorePackageVersion();
  const tree = scanCommandsDir(commandsDir);
  const files = listCommandFiles(tree);

  const routes: ManifestRoute[] = [];
  const records: Array<{
    path: string[];
    importPath: string;
    meta?: Meta;
    flags: FlagInfo[];
  }> = [];

  for (const entry of files) {
    const manifestPath = toManifestPath(entry.path);
    const labelPath = manifestPath.map((p) =>
      p.startsWith(":") ? p.slice(1) : p,
    );
    const mod = await loadAndValidateCommand(entry.file, labelPath);
    const flags = mod.args ? projectFlags(mod.args) : [];
    const importPath = (
      options.toImportPath ?? defaultImportPath
    )(entry.file, outFile);

    records.push({
      path: manifestPath,
      importPath,
      meta: mod.meta,
      flags,
    });

    routes.push({
      path: manifestPath,
      importPath,
      meta: mod.meta,
      flags,
      load: () => import(pathToFileURL(entry.file).href),
    });
  }

  const manifest: Manifest = {
    formatVersion: MANIFEST_FORMAT_VERSION,
    coreMajor: coreMajorFromVersion(coreVersion),
    routes,
  };

  const source = renderManifestModule(records, {
    formatVersion: MANIFEST_FORMAT_VERSION,
    coreMajor: coreMajorFromVersion(coreVersion),
    coreVersion,
  });

  mkdirSync(dirname(outFile), { recursive: true });
  writeFileSync(outFile, source, "utf8");

  return { outFile, manifest, routeCount: routes.length };
}

function toManifestPath(scanPath: string[]): string[] {
  return scanPath.map((p) => {
    const m = /^\[([^\]]+)\]$/.exec(p);
    return m?.[1] ? `:${m[1]}` : p;
  });
}

function defaultImportPath(commandFile: string, outFile: string): string {
  let rel = relative(dirname(outFile), commandFile).replace(/\\/g, "/");
  if (!rel.startsWith(".")) rel = `./${rel}`;
  // Keep extension so Node/tsx can resolve TypeScript sources in demos.
  return rel;
}

function renderManifestModule(
  records: Array<{
    path: string[];
    importPath: string;
    meta?: Meta;
    flags: FlagInfo[];
  }>,
  header: { formatVersion: number; coreMajor: number; coreVersion: string },
): string {
  const routes = records
    .map((r) => {
      const meta = r.meta ? `meta: ${JSON.stringify(r.meta)},` : "";
      return `  {
    path: ${JSON.stringify(r.path)},
    ${meta}
    flags: ${JSON.stringify(r.flags, null, 2).replace(/\n/g, "\n    ")},
    load: () => import(${JSON.stringify(r.importPath)}),
  }`;
    })
    .join(",\n");

  return `/** Generated by @clfly/core@${header.coreVersion} — do not edit by hand. */
/** @typedef {import('@clfly/core').Manifest} Manifest */

/** @type {Manifest} */
export const manifest = {
  formatVersion: ${header.formatVersion},
  coreMajor: ${header.coreMajor},
  routes: [
${routes}
  ],
};

export default manifest;
`;
}

function readCorePackageVersion(): string {
  const here = dirname(fileURLToPath(import.meta.url));
  for (const candidate of [
    join(here, "package.json"),
    join(here, "..", "package.json"),
    join(here, "..", "..", "package.json"),
  ]) {
    try {
      const raw = readFileSync(candidate, "utf8");
      const pkg = JSON.parse(raw) as { name?: string; version?: string };
      if (pkg.name === "@clfly/core" && pkg.version) return pkg.version;
    } catch {
      /* try next */
    }
  }
  return "0.0.0";
}
